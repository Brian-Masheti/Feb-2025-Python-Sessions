import numpy as np

my_array = np.array([1, 2, 3, 4, 5])
print("Array:", my_array) # Print the array

print("Array * 2:", my_array * 2) # Multiply each element by 2
print("Mean:", np.mean(my_array)) # Calculate the mean of the array
print("Sum:", np.sum(my_array)) # Calculate the sum of the array
print("Square Root:", np.sqrt(my_array)) # Calculate the square root of each element